public class evenandodd {
    private int number;
    public evenandodd(){
        this.number=number;
    }
    public boolean even(int number) {
        if (number % 2 == 0) {
            return true;
        } else
            return false;
    }
    public boolean odd(int number) {
        if (number % 2 != 0) {
            return true;
        } else
            return false;
    }
}
